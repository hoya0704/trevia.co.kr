<p align="left"><strong>2. </strong><strong>다음의 경우에는 예외적으로 고객의 동의 없이 개인정보를 제공할 수 있습니다.</strong></p>
<p align="left">- 형사소송법, 금융실명거래 및 비밀보장에 관한 법률, 신용정보의 이용 및 보호에 관한 법률, 지방세법, 소비자보호법, 한국은행법, 등 관계법령에 의하여 수사상의 목적으로 관계기관으로부터 요구가 있는 경우 </p>
<p align="left">- 통계작성 학술연구나 시장조사를 위하여 특정 개인을 식별할 수 없는 형태로 광고주, 협력사나 연구단체 등에 제공하는 경우 </p>
<p align="left">- 문화관광부 인증 우수상품에 관한 해당 관련기관의 요청이 있는 경우 </p>
<p align="left">- 서비스 제공에 따른 요금정산을 위하여 필요한 경우 </p>
