<p align="left"><strong>3. </strong><strong>동의거부권</strong></p>
<p align="left">개인정보주체자는 개인정보 제3자 제공에대한 동의를 거부할 권리가 있습니다. </p>
<p align="left">동의를 거부할 경우 상품서비스 수행에 필수적인 정보제공이 이뤄지지 않으므로, 예약수행이 불가함을 알려드립니다.</p>
